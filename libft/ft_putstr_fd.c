#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	if (s == NULL)
		return (0);
	i = -1;
	while (s[++i] != '\0')
		ft_putchar_fd(s[i], fd);
	return (i);
}
